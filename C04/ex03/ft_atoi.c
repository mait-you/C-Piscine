int	ft_atoi(char *str)
{
	int	i;
	int	s;
	int	x;
	int	result;

	i = 0;
	s = 1;
	x = 0;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			x++;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	if (x % 2 != 0)
		s = -1;
	return (result * s);
}
