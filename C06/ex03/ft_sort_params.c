#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	ft_strcmp(char *av1, char *av2)
{
	int	i;

	i = 0;
	while (av1[i] || av2[i])
	{
		if (av1[i] != av2[i])
			return (av1[i] - av2[i]);
		i++;
	}
	return (0);
}

void	ft_swap(char **av1, char **av2)
{
	char	*swap;

	swap = *av1;
	*av1 = *av2;
	*av2 = swap;
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		ft_putchar(str[i++]);
}

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 1;
	while (i < ac - 1)
	{
		j = 1;
		while (j < ac - 1)
		{
			if (ft_strcmp(av[j], av[j + 1]) > 0)
				ft_swap(&av[j], &av[j + 1]);
			j++;
		}
		i++;
	}
	i = 1;
	while (i < ac)
	{
		ft_putstr(av[i]);
		ft_putchar('\n');
		i++;
	}
	return (0);
}
