int	ft_strcmp(char *s1, char *s2)
{
	int		i;
	int		id;
	char	x;

	i = 0;
	id = 0;
	x = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i] && id == 0)
		{
			x = s1[i] - s2[i];
			id = 1;
		}
		i++;
	}
	return (x);
}
